import numpy as np

# Creating arrays
arr = np.array([1, 2, 3, 4, 5])
print("Array:", arr)

# Basic operations
print("Sum:", np.sum(arr))
print("Mean:", np.mean(arr))
print("Max:", np.max(arr))
print("Min:", np.min(arr))

# Creating a 2D array
matrix = np.array([[1, 2], [3, 4]])
print("Matrix:\n", matrix)

# Transpose
print("Transpose:\n", matrix.T)
